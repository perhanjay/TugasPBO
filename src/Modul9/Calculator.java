    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
        // TODO add your handling code here:
         bil2 = Double.parseDouble(jTextField1.getText());
        double hasil = 0;

        switch (pilih) {
            case 1:
                hasil = bil1 + bil2;
                break;
            case 2:
                hasil = bil1 - bil2;
                break;
            case 3:
                hasil = bil1 * bil2;
                break;
            case 4:
                hasil = bil1 / bil2;
                break;
            case 5:
                hasil = bil1 /100;
                break;
            case 6:
                hasil = bil1 % bil2;
                break;
            case 7:
                double x = bil1;
                hasil = Math.sqrt(x);
                break;
            case 8:
                hasil = bil1*bil1;
                break;
            case 9:
                hasil = bil1*bil1*bil1;
                break;
        }
        bil = "";
        jTextField1.setText(String.valueOf(hasil));
    }//GEN-LAST:event_jButton16ActionPerformed

